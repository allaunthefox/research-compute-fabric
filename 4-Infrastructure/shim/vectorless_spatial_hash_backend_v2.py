# §3  FIXED-POINT SCALARS (NO FLOAT IN COMPUTE PATHS)
# ============================================================

def to_q0_16(value: float) -> int:
    """Convert float to Q0_16 (0-255 range).
    
    This is only permitted at external boundaries (JSON parsing, sensor input).
    All internal computation uses integer Q0_16.
    """
    # Clamp to 0-255 range (Q0_16 bounds)
    scaled = max(0, min(255, int(value * 255)))
    return scaled


def to_q16_16(value: float) -> int:
    """Convert float to Q16_16 (signed 32-bit fixed-point).
    
    This is only permitted at external boundaries.
    All internal computation uses integer Q16_16.
    """
    # Q16_16: 16.16 fixed point, range [-32768, 32767.999985]
    # Scale by 65536 (2^16)
    scaled = int(value * 65536)
    # Clamp to Q16_16 range
    scaled = max(-32768 * 65536, min(32767 * 65536, scaled))
    return scaled


def from_q16_16(q16: int) -> float:
    """Convert Q16_16 to float (external boundary only)."""
    return q16 / 65536.0


