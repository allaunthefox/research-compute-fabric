# §5  HIERARCHICAL SPATIAL REFINEMENT (OCTREE)
# ============================================================

class HierarchicalSpatialBackend:
    """Hierarchical spatial refinement backend (octree-style).
    
    Addresses bucket scaling issue:
    - L0: 16×16×16 = 4096 cells (L1 cache, ~10 rows/cell target)
    - L1: 32×32×32 = 32768 cells (L2 cache)
    - L2: 64×64×64 = 262144 cells (main memory)
    
    Target: maintain ~10 rows per cell to preserve graph structure.
    """
    
    def __init__(self, max_levels: int = 3):
        self.max_levels = max_levels
        self.levels = []
        
        # Initialize level 0 (16×16×16)
        size = 16
        self.levels.append(np.empty((size, size, size), dtype=object))
        self._init_level(0, size)
        
        # Initialize higher levels on demand
        for level in range(1, max_levels):
            size = 16 * (2 ** level)
            self.levels.append(np.empty((size, size, size), dtype=object))
            self._init_level(level, size)
    
    def _init_level(self, level: int, size: int):
        """Initialize a level with empty cells."""
        for x in range(size):
            for y in range(size):
                for z in range(size):
                    base_coord = SpatialCoord(x % 16, y % 16, z % 16)
                    self.levels[level][x, y, z] = SpatialCell(
                        coord=base_coord,
                        voltage_mode=0,
                        density=0,
                        row_ids=[],
                        table_name=""
                    )
    
    def _get_level_for_density(self, coord: SpatialCoord) -> int:
        """Determine which level to use based on current density.
        
        Target: ~10 rows per cell to maintain graph structure.
        """
        for level in range(len(self.levels)):
            size = 16 * (2 ** level)
            level_coord = (coord.x % size, coord.y % size, coord.z % size)
            cell = self.levels[level][level_coord]
            if len(cell.row_ids) < 10:
                return level
        
        # If all levels are dense, use the highest level
        return len(self.levels) - 1
    
    def table_to_spatial_coord(self, table_name: str, row_id: int) -> SpatialCoord:
        """Map (table_name, row_id) to spatial coordinate using Morton code."""
        return table_row_to_morton_coord(table_name, row_id)
    
    def add_row(self, table_name: str, row_id: int, density: float = 0.0):
        """Add a row to the appropriate level of the hierarchy.
        
        Args:
            table_name: Source table name
            row_id: Row identifier
            density: Density value (float at external boundary, converted to Q0_16)
        """
        # Convert external float to internal Q0_16
        density_q0 = to_q0_16(density)
        
        coord = self.table_to_spatial_coord(table_name, row_id)
        level = self._get_level_for_density(coord)
        
        size = 16 * (2 ** level)
        level_coord = (coord.x % size, coord.y % size, coord.z % size)
        cell = self.levels[level][level_coord]
        
        cell.row_ids.append(row_id)
        cell.table_name = table_name
        cell.density = density_q0
        cell.write_count += 1
        cell.last_access_ts = int(time.time())  # Integer timestamp
        
        # Call LEAN classifier (Python wrapper only, no decision logic)
        cell.voltage_mode = lean_classify_voltage_mode(
            cell.write_count,
            cell.read_count,
            from_q16_16(cell.delta_variance),
            from_q16_16(to_q16_16(0.1))  # threshold = 0.1 as Q16_16
        )
    
    def get_cell(self, table_name: str, row_id: int, level: int = 0) -> SpatialCell:
        """Get cell at specific level."""
        coord = self.table_to_spatial_coord(table_name, row_id)
        size = 16 * (2 ** level)
        level_coord = (coord.x % size, coord.y % size, coord.z % size)
        cell = self.levels[level][level_coord]
        cell.read_count += 1
        cell.last_access_ts = int(time.time())
        
        # Re-classify using LEAN classifier
        cell.voltage_mode = lean_classify_voltage_mode(
            cell.write_count,
            cell.read_count,
            from_q16_16(cell.delta_variance),
            from_q16_16(to_q16_16(0.1))
        )
        
        return cell
    
    def update_delta_variance(self, table_name: str, row_id: int, delta: float):
        """Update running variance of deltas using fixed-point arithmetic.
        
        Uses Welford's online algorithm with Q16_16 fixed-point.
        """
        # Convert external float to internal Q16_16
        delta_q16 = to_q16_16(delta)
        
        coord = self.table_to_spatial_coord(table_name, row_id)
        level = self._get_level_for_density(coord)
        size = 16 * (2 ** level)
        level_coord = (coord.x % size, coord.y % size, coord.z % size)
        cell = self.levels[level][level_coord]
        
        # Online variance update (Welford's algorithm) with fixed-point
        n = cell.write_count
        if n == 1:
            cell.mean_delta = delta_q16
            cell.m2 = 0
        else:
            # Fixed-point arithmetic: all in Q16_16
            delta_n = delta_q16 - cell.mean_delta
            cell.mean_delta = cell.mean_delta + (delta_n / n)
            delta_n2 = delta_q16 - cell.mean_delta
            cell.m2 = cell.m2 + (delta_n * delta_n2)
        
        if n > 1:
            cell.delta_variance = cell.m2 // (n - 1)
    
    def get_neighbors(self, table_name: str, row_id: int, level: int = 0) -> List[SpatialCell]:
        """Get neighbors at specific level (3 ≤ neighbors ≤ 26).
        
        Correctly handles edge/corner cases where neighbor count < 26.
        """
        coord = self.table_to_spatial_coord(table_name, row_id)
        size = 16 * (2 ** level)
        neighbors = []
        
        for dx in [-1, 0, 1]:
            for dy in [-1, 0, 1]:
                for dz in [-1, 0, 1]:
                    if dx == 0 and dy == 0 and dz == 0:
                        continue
                    
                    nx, ny, nz = coord.x + dx, coord.y + dy, coord.z + dz
                    
                    # Check bounds (no toroidal wrapping)
                    if 0 <= nx < size and 0 <= ny < size and 0 <= nz < size:
                        neighbors.append(self.levels[level][nx, ny, nz])
        
        return neighbors
    
    def get_grid_statistics(self) -> dict:
        """Get statistics about hierarchical grid utilization."""
        stats = {
            'levels': len(self.levels),
            'level_stats': []
        }
        
        for level, grid in enumerate(self.levels):
            total_cells = grid.size
            occupied_cells = 0
            total_rows = 0
            avg_density = 0
            max_occupancy = 0
            
            for x in range(grid.shape[0]):
                for y in range(grid.shape[1]):
                    for z in range(grid.shape[2]):
                        cell = grid[x, y, z]
                        if cell.row_ids:
                            occupied_cells += 1
                            total_rows += len(cell.row_ids)
                            avg_density += cell.density
                            max_occupancy = max(max_occupancy, len(cell.row_ids))
            
            if occupied_cells > 0:
                avg_density //= occupied_cells
            
            level_size = 16 * (2 ** level)
            stats['level_stats'].append({
                'level': level,
                'size': f"{level_size}×{level_size}×{level_size}",
                'total_cells': total_cells,
                'occupied_cells': occupied_cells,
                'utilization': occupied_cells / total_cells if total_cells > 0 else 0,
                'total_rows': total_rows,
                'avg_density': avg_density,
                'max_occupancy': max_occupancy,
                'avg_rows_per_cell': total_rows / occupied_cells if occupied_cells > 0 else 0
            })
        
        return stats


