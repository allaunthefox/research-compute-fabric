# §2  SPATIAL COORDINATE WITH MORTON CODE (LOCALITY-PRESERVING)
# ============================================================

@dataclass
class SpatialCoord:
    """16×16×16 spatial coordinate. Each axis is 4 bits (0-15)."""
    x: int  # 0-15 (4 bits)
    y: int  # 0-15 (4 bits)
    z: int  # 0-15 (4 bits)
    
    def to_morton(self) -> int:
        """Convert to Morton code (Z-order curve) - locality-preserving."""
        morton = 0
        for i in range(4):  # 4 bits per coordinate
            morton |= ((self.x >> i) & 1) << (3 * i + 0)      # x bits at positions 0,3,6,9
            morton |= ((self.y >> i) & 1) << (3 * i + 1)      # y bits at positions 1,4,7,10
            morton |= ((self.z >> i) & 1) << (3 * i + 2)      # z bits at positions 2,5,8,11
        return morton
    
    @staticmethod
    def from_morton(idx: int) -> 'SpatialCoord':
        """Convert from Morton code - locality-preserving."""
        x, y, z = 0, 0, 0
        for i in range(4):
            x |= ((idx >> (3 * i + 0)) & 1) << i
            y |= ((idx >> (3 * i + 1)) & 1) << i
            z |= ((idx >> (3 * i + 2)) & 1) << i
        return SpatialCoord(x, y, z)
    
    def __hash__(self) -> int:
        return self.to_morton()
    
    def __eq__(self, other) -> bool:
        if not isinstance(other, SpatialCoord):
            return False
        return self.x == other.x and self.y == other.y and self.z == other.z


def table_row_to_morton_coord(table_name: str, row_id: int) -> SpatialCoord:
    """Map (table_name, row_id) to Morton coordinate - locality-preserving.
    
    Uses row_id sequential bits to preserve locality: row_id N and N+1 are nearby.
    Table name hash provides spatial rotation to prevent clustering.
    """
    import hashlib
    
    # Extract sequential bits from row_id (preserves locality)
    row_bits = row_id & 0xFFF
    
    # Use table name hash for spatial rotation (prevents clustering)
    table_hash = int(hashlib.md5(table_name.encode()).hexdigest()[:8], 16)
    
    # XOR with table hash for distribution while preserving locality
    morton = row_bits ^ (table_hash & 0xFFF)
    
    return SpatialCoord.from_morton(morton)


